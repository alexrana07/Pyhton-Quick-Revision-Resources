"""
1.What is a Data Type?
ans:A data type tells Python what kind of value a variable is holding and what operations can be done on it.
In simple words:
Data type = type of data stored in a variable

2.Python has built-in data types, which can be broadly classified into two types:

A.Primitive Data Types,Primitive data types store single, simple values.
Example:
int
float
str
bool

B.Non-Primitive Data Types,Non-primitive data types store multiple values or complex data structures.
Example:
list
tuple
set
dict


"""

# Primitive data types

# 1. int (Integer) - This integer data type stores the whole nubmers.
#example:
age = 18
marks = 90

#2. float - this float data type Stores decimal numbers.
#example:
price = 99.5
height = 5.8

#3.str (String) - this string data type Stores text (characters).
#example:
name = "Alex"
city = "Ahmedabad"

#4. bool (Boolean) - this bool data type Stores True or False.
#example:
is_student = True
has_passed = False



#Non-Primitive Data Types

#1. list - Stores multiple values (changeable)
#example:
marks = [80, 85, 90]

#2.tuple - Stores multiple values (not changeable)
days = ("Mon", "Tue", "Wed")

#3. set - Stores unique values
#example:
numbers = {1, 2, 3}

#4. dict (Dictionary) - Stores data in key-value pairs
#example:
student = {
    "name": "Ali", 
    "age": 18
    }




